package pkg2forma.abs;

/**
 *
 * @author gugaa
 */
class Retangulo extends Forma {

    double largura;
    double altura;

    Retangulo(double largura, double altura) {
        this.largura = largura;
        this.altura = altura;
    }

    @Override
    double calcularArea() {
        return largura * altura;
    }
}

